    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        try {
            if (txtIDR.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this,
                    "Seleccione un usuario para poder modificar",
                    "Sin usuario seleccionado",
                    JOptionPane.WARNING_MESSAGE);
                return;
            }

            int id = Integer.parseInt(txtIDR.getText());
            
            
            Roles rol = new Roles(id, txtUsuario.getText(),txtRol.getText() );

            rol.modificarUsuario();
            limpiar();

            modeloTabla = Roles.gestiondeRoles();
            tbRoles.setModel(modeloTabla);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                "El Campo de id usuario debe ser numericos",
                "Datos no numericos",
                JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try {
            
            Roles nuevorol = new Roles(txtUsuario.getText(), txtRol.getText());

            nuevorol.creacionDeUsuarios();
            limpiar();

            modeloTabla = Roles.gestiondeRoles();
            tbRoles.setModel(modeloTabla);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                "El Campo de usuario debe ser numericos",
                "Datos no numericos",
                JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        if (txtIDR.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Seleccione un Rol para poder eliminar",
                "Sin rol seleccionado",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        int opt = JOptionPane.showConfirmDialog(this,
            "¿Desea eliminar el usuario con el nombre: " + txtUsuario.getText(),
            "Eliminar usuario",
            JOptionPane.OK_CANCEL_OPTION);

        if (opt == 0) {
            int codigo = Integer.parseInt(txtIDR.getText());

            Roles rol = new Roles(codigo);

            rol.eliminarUsuarios();
            limpiar();

            modeloTabla = Roles.gestiondeRoles();
            tbRoles.setModel(modeloTabla);
        } else {
            JOptionPane.showMessageDialog(this, "Eliminacion cancelada");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

